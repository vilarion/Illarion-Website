<h2>-2,588 until -2,579: The Second Orcish Wars</h2>

<p>In the forests of Eldaril, the Howling Wolf Clan encountered the elvenkind. These
elves had been maintaing trade relationships with the Plainfolk humans. Thus, they head
heard rumours of the orcs, who supposedly stood in alliance with demons. They considered
this thesis as confirmed by the hunters of the orcs having (unknowingly) trespassed into
their sacred grounds, and having tarnished the latter with the blood of slain animals.
The Howling Wolf Clan was quickly driven out of the forest. The elves applied
exceptional precision in the task-the archers used magic arrows in order to mortally
wound with their first strike and spare their enemies of any suffering. If that had not
been enough already, the archers also used magic in order to remain unseen.</p>

<p>The orcish clans gathered and did council.</p>

<p>Shortly thereafter, the orcs returned and set a widespread fire to the forest
borders. It took several weeks until the quickly-spreading fire was completely
extinguished. A humongous woodland range and three towns of the elvenkind were victims
to the flames, and those survivors set aghast were captured by the orcs moving up upon
the following eve. According to orcish tradition, the most severely wounded captives
were sacrificed to the Gods in gratitude for the victory-due to the lack of any other
foodstuffs, they were eaten.</p>

<p>Merely three days later, still in the middle of victory celebrations, the encampment
of the orcs was attacked. Numerous elves had appeared, clad in weapons and armour from
times of ancient wars and such that were crafted to follow the footsteps of ancient
arts. Elven magic cast breaches into the orcish battlements, and while the captives took
their flight, wardancers and bladesingers already lept into the midst of the stupefied
orcs. It took only little time until the stupefaction of the orcish warriors transformed
into a wild escape: There was nothing to set against an enemy that moved supernaturally
fast, could hurl blaze and lightning, and would appear in the middle of their own rows.
Nearly half of all the present orcs were slain immediately during this surprise attack.
Because the elves had counted their losses as well, they decided against chasing the
escapees, and instead decided to wait for reinforcements and honor the fallen.</p>

<p>The eldest of the Waraxe Clan decided to distance himself as far as possible from the
elven territory, because first of all a war was unnecessary and secondly the tribes were
weakened by famine and battle and would not withstand the foe. The Howling Wolf Clan
followed this decision, while the Nighthawk Clan wanted to continue doing battle. The
Bloody Skull Clan decided to send negotiators with gifts to the elves. None of the
parliamentaries ever arrived.</p>

<p>In the following ten years, there were several skirmishes between orcs and elves,
while the eldest debated over how to deal with the situation. The orcs had been so
weakened by the years of wandering that they posed no grand threat, however, they had
learned to craft and use the bow and blade. This debate went on for centuries.</p>

