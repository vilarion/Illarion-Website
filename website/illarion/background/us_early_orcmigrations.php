<h2>-2,678 until -2,612: The Initial Orcish Wars</h2>

<p>The warlord of the Dragonclaw Clan, Tuchak Bloodfist, takes over the leadership of
the clan with some intrigues and several murders. He names himself the warlord over all
orcish kin. He has the eldest of the other two major clans (Wolfmoon and Firefist)
murdered and replaces them with warmongers loyal to him. In the following rebellions and
insurrections, Tuchak Bloodfist is slain, and the three major clans split up into five
(Bloodfist, Nighthawk, Howling Wolf, Waraxe, and Bloody Skull Clan), as well as some
several minor ones.</p>

<p>The Bloodfist Clan, which consists of former followers of Tuchak, is driven away by
the other four clans and begins to wander. Now led by Krugg Bloodfist (a younger brother
of Tuchak), the clan robs and loots two settlements of the plainfolk humans. In the
following years, the Bloodfist Clan presses on into civilized territory, and scouts of
the orcs report of the city of Albar.</p>

<p>Krugg was astonished by message of a fortified settlement of such a vast size and
knowing the Albarian army was closely following his tracks, the leader of the Bloodfist
Clan withdrew and sent four messengers to the four other major Clans. These deny the
Bloodfist any support though. The Bloodfist ist nearly wiped out. In order to save his
clan from annihilation, Krugg sacrificed thirty captives and himself in a blood ritual.
Shortly thereafter, a giant horde of monsters led by a demon decimates the Albarian
army. The rest of the Bloodfist Clan withdraws in the meanwhile to the security of the
borders to the orcish territories.</p>

<p>The message of demon's magicks, monsters, and bloodthirsty orcs spreads quickly in
the cities of mankind. Salkamaerian paladins hire mercenaries to support their former
enemy city of Albar, and the new legion quickly finds further support in the Serinjah
tribes. The Bloodfist Clan is wiped out and scattered. The human army advances deeply
into the orcish territory and builds a row of fortified outposts along the Draikon
river. Along the course of centuries to come, attacks against the orcish clans are lead
from here onwards.</p>

