<h2>-2482 bis ca.-2330 Die Dunkelelfen</h2>

<p>Die oberste des Hauses Narnil, Onathrae Nirié Narnil, führt das Haus auf den Spuren
der Orks bis an den Fuß des Aramon-Gebirges. Da die Orks das Gebiet vor etwa 60 Jahren
durchquert hatten, sind die Spuren kaum noch auffindbar. Die .erume-lie. - .die (Leute),
die Verlassen haben. , so werden die Dunkelelben von den Elfen genannt - finden
verfallene Bauten in einem Gebirgstal. Für sie ist klar : Die Orks müssen die
Bevölkerung geschlachtet haben. Das es kein Zeichen von Krieg gibt, wird angenommen, die
Bewohner waren friedliche Handwerker, die sich nicht wehren konnten.</p>

<p>Ein Späher des Hauses findet im Gebirge einen Stolleneingang. Selbiger führt in eine
verlassene Zwergenmine. Kundschafter stellen fest, dass unterirdische Gänge bis tief in
die Berge führen.</p>

<p>Das Haus Nanril zerstreitet sich: Während Onathrae Nirié weiterhin die Orks verfolgen
will, halten viele diese Jagd für sinnlos. Einige wollen zu den Städten der Elfen
zurückkehren, andere die Gänge erforschen. Onathrae Nirié Narnil wird eines Abends
erstochen in ihrem Lager aufgefunden, nachdem sie sich zuvor lautstark mit ihren
Schwestern gestritten hatte. Beide setzen sich durch, und entscheiden, die Höhlen zu
erforschen.</p>

<p>Die Späher treffen auf Wächter der Zwerge. Die Gespräche verlaufen reserviert, aber
friedlich. Die Zwerge verbieten das Betreten ihrer Stollen, erlauben jedoch die
Erkundung der Höhlen. Da das Tageslicht in ihren Augen schmerzt, entschließen die
Schwestern, fortan in den Höhlen zu leben, und Nachts Jäger an die Oberfläche zu
schicken. Die meisten älteren Mitglieder des Hauses sind mit diesem Entschluss sehr
unzufrieden. </p>

<p>In den nächsten Jahrzehnten erforschen und besiedeln die Dunkelelben einen
Höhlenkomplex, der weiter in die Tiefe hineinreicht als selbst die Stollen der Zwerge:
Dort gibt es alte Ruinenreste, welche von den Zwergen als bösartiger Ort gemieden
wurden. Es kommt zu kleineren territorialen Konflikten, welche allerdings verbal gelöst
werden. Ein großer Teil der Dunkelelben spaltet sich vom Hause Narnil ab. Sie brechen
auf, um zu den anderen Elfen zurückzukehren. Etwa um -1390 erreichen die Abgespaltenen
die alten Siedlungen der Elfen. Sie werden jedoch als Ausgestoßene betrachtet, als
Gottlose und Verfluchte. Anstatt ihnen die Häuser und Eigentümer des Hauses Narnil
zurückzugeben, werden sie aufgefordert, die Götter und den Ältestenrat zuvor um
Verzeihung zu bitten.</p>

<p>Anstatt sich dem zu fügen, stehlen einige der Dunkelelben zwei Artefakte aus dem
Tempel der fünf, welche sich vormals im Besitz des Hauses Narnil befunden hatten: Ein
facettierter Edelstein, welcher der Legende nach blutrot wurde, als der Tempelwächter
it einem Messer im Rücken starb - fortan als der "Blutstein" bezeichnet - sowie das alte
Wahrzeichen des Hauses Narnil, ein silberner Pokal in Form einer Firnisblüte. Als der
Tempelwächter am nächsten Morgen gefunden wurde, wurden sofort die "Verirrten" dafür
verantwortlich gemacht. Selbige stritten die Tat ab. Am Ende wurden die erume-lie vom
Ältestenrat aus der Stadt verbannt.</p>

<p>Die Verbannten kehrten zu den Höhlen zurück, denn das Tageslicht verursachte in ihren
Augen Schmerzen. Dort war es inzwischen zu Streitigkeiten mit den Zwergen gekommen: Die
inzwischen besiedelten und wiederaufgebauten Ruinen galten bei den Zwergen als
verflucht. Ein Wiederaufbau kam einer Blasphemie gleich. Außerdem kontrollierten die
Zwerge die einzigen Wege zurück zur Oberfläche. Jäger der Dunkelelben hatten dennoch
immer öfter Herdentiere der Zwerge an der Oberfläche gejagt.</p>

<p>Am Ende längerer Auseinandersetzungen wanderten die Dunkelelben weiter und weiter in
die Höhlen aus: Die Zwerge hatten Verstärkung aus anderen Siedlungen erbeten, sie
eroberten und zerstörten die Ruinen völlig. Sie eroberten auch das Wahrzeichen des
ehemaligen Hauses Narnil, den Kelch in Form einer Firnisblüte.</p>

<p>Immer wieder wurden zwergische Arbeitstrupps in ihren tieferen Minen überfallen, bis
Priester der Zwerge die Zugänge zu den Tiefen Höhlen zum Einsturz brachten.</p>

<p>Es heißt, das Haus Narnil sei damit untergegangen. Doch Berichten zu Folge sind
vereinzelte Dunkelelben-Gruppen auch in völlig anderen Gegenden, weitab vom
Aramon-Gebirge aufgetaucht.</p>

