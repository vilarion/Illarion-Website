<h2>-2329 Aufstände in Albar</h2>

<p>In Albarischen Landen kommt es zu einem Leibeigenenaufstand. Der Aufruhr wird durch
die königlichen Garden blutig niedergeschlagen. Da die Anführer des Aufstandes es
schafften, nach Salkamar zu flüchten, werden stattdessen jeweils die nächsten Verwandten
zum Tod durch den Strang verurteilt, welche nicht ebenfalls geflohen sind, zwölf Bauern
werden auf der Flucht gefangengenommen und hingerichtet. In dem Jahr fliehen über
fünftausend Leibeigene nach Salkamar.</p>

