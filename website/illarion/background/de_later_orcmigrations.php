<h2>-2497 Die Ork-Züge - Neues Land</h2>

<p>Der Bloody-Skull-Clan, der Waraxe-Clan, der Nighthawk-Clan und der Howling-Wolf-Clan
sind lange über unbesiedeltes, jedoch felsiges und wenig fruchtbares Land gereist. Die
Clans folgten dem Flusslauf des Andrilai-Flusses. Um schneller zu Reisen und möglichst
vor dem Winter noch fruchtbares Land zu finden, sammelten die Stämme Holz aus allen
Wäldern, die auf ihrem Weg liegen - es wird zu Flößen gebunden. Die Stämme erreichen
den Talith-See, in welchem der Andrilai und zwei weitere Flüsse sich zum Vorthain-Strom
verbinden.</p>

<p>Grubuk Donnerhand, Ältester Seher des Bloody-Skull-Clans, ruft alle Clans am Ufer des
Sees zusammen, und erklärt, ein Falke habe ihm im Traum den Weg in eine neue Heimat
gewiesen. Ein Schwarzer Fluss, der in den See münde, würde sie in ihre neue Heimat
führen.</p>

<p>Im Frühling umreisen die Stämme den See, überqueren mit ihren Flößen die Mündung des
Flusses, der heute Dornenwasser genannt wird, und schlagen ihr Lager an der Mündung des
Schwarzstrom auf, um auf Nachzügler zu warten.</p>

<p>Drei Tage später schwillt der Schwarzstrom stark an. Schmelzwasser aus den Bergen
bringt schwarzen Schlamm mit sich, welchem der Fluss seinen Namen verdankt. Unter großem
Jubel folgen die Stämme dem Strom flussaufwärts bis in die Hochebenen von Norbuk, was
"Starkes Land" bedeutet. Um -2495 siedeln die Stämme in den Ebenen.</p>

