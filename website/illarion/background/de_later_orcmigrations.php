<h2>-2503 Der Fall des Hauses Narníl</h2>

<p>Onathrae Nirié Narnil, die Älteste des Elfenhauses Narnil, war eine Überlebende
Gefangene des Angriffes der Orks. Sie hatte der Legende nach mit ansehen müssen, wie
ihre Eltern von Orks erschlagen und einer ihrer Brüder geopfert und gegessen wurden.
Während der 75 Jahre, in denen der Ältestenrat über die Orks beriet, waren sie und ihre
Schwestern diejenigen, die am vehementesten dafür eintraten, die Orks ein für allemal zu
vernichten.</p>

<p>Einige Gefangene Orks hatten sich jedoch über die Jahre hinweg mit manchen der Elfen
angefreundet. Sie hatten sich sogar als ehrenhaft und gelehrig erwiesen, wenn auch ihr
aufbrausendes Talent die Ältesten sehr beunruhigte - jedoch war sich der Ältestenrat
über die Situation der Orks klar geworden. Sie waren Vertriebene, und für dämonische
Magie war kein Hinweis vorhanden.</p>

<p>Als der Rat entschied, die ohnehin sehr geschwächten Clans nicht mehr anzugreifen und
auch keine Verfolgung einzuleiten, rief Onathrae, außer sich vor Wut, die Götter an. Als
sie keine Antwort erhielt, rief sie das Haus Narnil zu den Waffen, um die Clans auf
eigene Faust zu vernichten.</p>

<p>Es heißt, dass in diesem Moment ein Priester in ihren Weg trat und verkündete, dass
Elara, Zhambra, Sirani und Malachín eine Verfolgung oder gar Auslöschung der Clans
untersagten. Man dürfe kein Volk wegen der Verbrechen Einzelner vernichten. Auch wäre
die Vernichtung eines ganzen Volkes eine unduldbare Blasphemie gegen den Willen der
Fünf.</p>

<p>Onathrae erschlug den Priester auf der Stelle und floh. Kurz darauf brachen sie, ihre
Schwestern und das gesamte Haus Narnil auf, um Orks zu jagen.</p>

<p>Es heißt, dass in diesem Moment sie und ihre Schwestern von den Göttern mit einem
Fluch belegt worden seien. Von diesem Tag an war es ihnen unmöglich, am Tage zu reisen -
ihre Augen konnten die Helligkeit der Sonne nicht mehr ertragen, so dass sie nur des
Nachts reisen konnten. Es heißt, dass das gesamte Haus Narníl daraufhin den Göttern
abschwor. Des Nachts folgten sie den verhassten Orks, die inzwischen weit weg über die
Berge gezogen waren.</p>

