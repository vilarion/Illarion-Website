<h2>-2542 bis -2537 Der Abstieg der Zwerge</h2>

<p>Späher des Waraxe-Clans treffen in einem Gebirgstal des Aramon-Gebirges auf eine
Siedlung von Zwergen. Die Zwerge, welche in großangelegten Minen Jahrhundertelang Erze
und Edelsteine zu Tage förderten, verfügen über Werkzeuge, Plattenrüstungen und Waffen
aus gehärtetem Stahl - die verblüfften Krieger des Waraxe-Clans müssen feststellen, dass
ihre Speere und Pfeile beinahe wirkungslos vom Gegner abprallen. Die Plünderung einer
zwergischen Siedlung erweist sich somit als äußerst schwierige Angelegenheit. Der
Waraxe-Clan zieht sich schnell zurück, um die anderen Clans zu verständigen und vor den
"Kleinen, glänzenden Kriegern" zu warnen.</p>

<p>Alarmiert durch den Kontakt mit den Orks, beschließt der Ältestenrat der Zwerge, die
Siedlungen selbst in die Minen zu verlegen. Erstens böte man so eine geringere
Angriffsfläche, zweitens hätte man so die Gelegenheit, weitaus tiefere Stollen anzulegen
und noch tiefer zu graben. Die ohnehin schon als Lagerhallen genutzten Minenhöhlen
werden als Wohnhöhlen ausgebaut, die Eingänge getarnt.</p>

<p>Vier Jahre später kehren die orkischen Stämme zurück. Als die Späher, auf dem Weg
durch das Gebirge, mehrere völlig ausgeräumte Siedlungen vorfinden - und keinen einzigen
Zwerg - werden die Orks misstrauisch. Sie entsenden Späher in alle Richtungen.
Diejenigen unglücklichen Späher, welche wirklich in die Nähe einer Zwergenmine gelangen,
kehren nie zurück. Es entstand die Theorie, dass Böse Geister in den Bergen die Zwerge
und auch die Späher gefressen hatten...</p>

<p>Das Aramon-Gebirge wurde von den Orks daraufhin so schnell wie möglich überquert, und
galt lange Zeit als verfluchtes Land.</p>

