<h2>-1,982 until -1,774: The Centennial Wars</h2>

<p>The human cities war against eachother for over two hundred years. The other races
are successful in staying out of the conflicts (except for various groups of
mercenaries, of course). Towards the end of the Centennial Wars, an orcish warhorde
takes the chance and loots settlements of humans and of halflings. Consequently, most of
the towns and cities of the humans are vanquished and destroyed.</p>

<p>Towards the end of the conflict, there are only three of the previously five large
human cities left: The capitols of the old realms Albar and Salkamar, and the
independant pirate haven of Gynk, which was founded by war refugees. The wars were over,
because Salkamar had sealed a pact to ensure no further attacks and future support to
eachother, and to drive away the orcish horde. Finally, Albar also entered the pact in
order to not be prone to the danger of facing war with the growing alliance of former
enemies.</p>

<p>Because the orcish horde flees to Norodaj territories and lands in the middle between
norsemen and dwarvenkind, they are obliterated.</p>

