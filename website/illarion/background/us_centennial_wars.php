<h2>-1,774: The Shadow Winter</h2>

<p>In the year of -1,774, a plague of famine breaks out in the city of Albar in winter.
It is stated within old records that the Albarian King Theoderich the II. had ordered to
multiply the Tenth by five, because his corn and treasure reserves had emptied (the
reason hitherto was that these resources were spent on his legions). In consquences to
this act, nearly one fourth of the non-noble populace died of starvation during the
freezing winter. Another fourth of the surviving peasants (the large part of the
populace) escaped the coming spring to Gynk and Salkamar. It is assumed that about one
third of the refugees died either by weakness, the warm swamplands of Gynk, or by the
hand of Albarian guards. Theoderich the II. dies by a poisoned arrow in the following
summer, which struck his heart while hunting.</p>

