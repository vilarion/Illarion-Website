<h2>-1,812 until -1,811: Fall of the Fortress of Khalm</h2>

<p>After the long wars with the Salkamaerian border city Naskur, the legendary Albarian
fortress Khalm (upon the mountain of Khalm, which shared its name with the city around
the base of the mountain) is ambushed by an orcish warrior horde and falls under siege.
It is cut within the time frame of a year. The Albarian army was currently away on an
advance into Salkamaerian territory and was not ordered back for defence. It is said
that the guardless and hungered-out (from the previous decade of war) city surrendered
to the orcish warriors without battle.while only fifty defenders had held the fortress
on the mountain for almost a whole year. The contemporary Lord Lhoderik of Khalm
requested reinforcements from the Albarian king. It is said that he had cursed out loud
for several days upon receiving the reply:</p>

<p style="font-style:italic;">"We, Theoderich the II., son of the glorious Wieganth,
inheriter of the old race of Onatheris, King of Albar and future conquerer of Salkamar,
are not aware of any necessity to consider withdrawing our victorious forces from the
borders of Salkamar.<br />We, Theoderich the II., deem it much more appropriate to place
our full trust into the strength of your defenses. We would like to remind you that an
equipped Albarian warrior, of which you should have hundreds, should be capable of
facing the challenge of ten or even twenty of the barbaric creatures by yourselves,
given a proper commanding.<br />Additionally, we notice regretfully that you again have
failed your duty of paying the Tenth. We expect from you, servant, that you will soon
comply to your duties."</p>

<p>According to legend, the severed head of Lhoderik of Khalm, who fell to the axe of an
orcish warrior, was impaled on an orcish spear and even beyond death was cursing at the
stupidity and arrogance of his king, and still under many curses, asked the orcs for the
favour to beat the living hells out of Theoderich in order for him to finally find
peace.</p>

<p>But because the orcish horde was defeated before they could near Albar, nothing
became of it, and it is said that the spirit of Lhoderik still wanders cursingly through
the ruins by full moon.</p>

