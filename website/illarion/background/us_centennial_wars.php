<h2>-1,777: Looting of Firnismill</h2>

<p>The orcish warhordes loot and set fire to the halfling town Firnismill and its
surrounding territory. The motives behind these acts are to gather provisions for the
planned war against Albar and to send slaves back to their home grounds.</p>

<p>The halflings in the area are subjugated, but they organise themselves in an
effective underground resistance movement. Their sheer toughness and thick-headedness
causes them to actually garner respect from their occupiers.</p>

<p>Orcish proverbs from the time say:<br />
<i>"A dead halfling is honored by hundreds,"<br />
"The loudest tyrant dies by a fist in his sleep,"<br />
and "Trust no cook who is your slave." </i></p>

<p>Merely two months later, a legion of elvenkind and dwarves arrives, which in the name
of the Aramon-Alliance attempts to drive the orcish horde out of Firnismill. They are
supported by a very angry mob of halflings.</p>

