<h2>-1,702: Insurrections in Albar</h2>

<p>Heavy riders beat down new insurrections led by the peasantry. The peasant leader is
whipped to death in public. According to legend, it is said that "Rigar the Brave" had
neither screamed nor shown any sign of pain.</p>

<p>Old documents reveal that he had hung himself in his cell beforehand. How the
whipping could have then still taken place would be questionable, as such, these
documents are considered to be false. Some scholars hold the opinion that magic or
decromancy might have been used to move the dead body, just in order to carry out the
execution. However, the Albarian crown placed bounties onto the heads of any scholar who
would profess such a thesis.</p>

