Zerstörung und Verzweiflung herrschen würde. Moshran, der mit Unterstützung seiner drei Helfern
im Verborgenen wirken kann, beginnt nun seine Suche nach den mächtigen Artefakten, seinem
Schwert und seiner Rüstung, die ihm genommen worden waren. Mit der Macht, die ihnen inne wohnt,
will er seine einst uneingeschränkte Gewalt zurückerlangen.</p>

<p>Und so endet das Zeitalter der Einsamkeit. Möge sich unser kommendes Schicksal als gnädig
