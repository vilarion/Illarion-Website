<h2>-1,583 until -1,579: The Plague Summer of Gynk</h2>

<p>In the late summer of the year -1,583, a plague breaks out in Gynk. Presumably the
pestilence took its beginning somewhere deep within the warm Gynkeesh swamps. The
damp-warm climate of the swamps, upon which Gynk was built, and the swarms of mosquitoes
seem to favour the illness. The wealthier deal of the populace burns expensive tree
resins in order to drive the mosquitoes from their chambers and are by far less
susceptible fo the disease. Approximately one fourth of the poorer populace is eaten
away by the plague, while the wealthy citizens find healing with priests and magi. The
disease is never completely eradicated in the following years. The populace however
develops a certain resistance towards the illness, up to the point where only old and
weakly citizens fall victim to it.</p>

<p>Recognizable signs of this disease are a strong trembling, feeling dizzy, losing
sense of direction; and in later stages, muscular failure, severely ecstatic
hallucinations and complete loss of senses, which ultimately leads to death.</p>

<p>In the early spring of -1,579, a Gynkeesh hermit finds out that a typical swamp-based
plant, the Stingweed, can heal the disease, if consumed in form of tea.</p>

<p>A side effect of the tea is a very light and rather relaxing and revitalizing state
of intoxication, as well as refreshing the magical power of the person drinking it.
Stingweed tea becomes a hit in exported goods from Gynk. The tea had already been known
to elvenkind for centuries and was oftenly consumed in their circles before meditations.
The trade with Gynk spares the elves the task of searching the uninviting swamps.
Serving this tea furthermore becomes a fad throughout the taverns of Salkamar and Albar,
even temples acquire and consume large amounts of it. As yet another consequence of this
discovery, several scholars and herbalists traveled to Gynk to explore the swamps in
search of other unknown herbs.</p>

<p>The surroundings of Gynk are ever since plastered with large plantations solely
farming Stingweed. A Gynkeesh legend of a giant swamp crocodile possibly stems from this
time. The myth was presumably spread in order to protect a larger amount of natural
Stingweed growth before it was so successfully farmed.</p>

