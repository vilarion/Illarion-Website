<h2>-1,439: The Snakevesper Sect</h2>

<p>Ruins are discovered within the deeper swamps of Gynk.</p>

<p>Apparent symbol of this cult are roughly-hewn statues of humans whose lower halves of
the body are serpentine, like large snakes. There is nothing else of value in the ruins
aside from old pieces of pottery. Furthermore, no graves or tombs are to be found, as it
is likely that this ancient culture buried their dead in the swamps.</p>
<p>Scholars find out quickly that the depictions are probably a reaction of a primitive
culture having met the lizardfolk. Records of the nearest Tanora temple support this
thesis. But because these ruins are deemed uninteresting, they are are not any further
exposed and left to the swamps.</p>

<p>Jaergen Erainek, a former Salkamaerian novice of Elara who was "relieved" of his
duties due to his preference of overbearing amounts of Stingweed and other herbs he
consumed, and ever since had delivered his assistance to expeditions, claims
otherwise.</p>

<p>According to him, the primitive statues are depictions of a forgotten deity. He is
laughed at for this theory though. Three years later however, he has gathered an
astonishing amount of followers who see the "prophet of glorious enlightenment" in him.
These followers have built domiciles in the ruins. Their main religious practices
consist of the consumption of herbs that induce hallucinogenic effects, and they export
these herbs.</p>

