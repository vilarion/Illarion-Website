<h2>-1583 bis -1579 Die Pestsommer von Gynk</h2>

<p>In Gynk bricht im Spätsommer des Jahres 1583 eine Pest aus, die vermutlich irgendwo
in den warmen Gynkesischen Sümpfen ihren Anfang nahm. Das feuchtwarme Klima der Sümpfe,
über denen Gynk erbaut wurde, sowie die Schwärme von Mücken scheinen die Krankheit zu
begünstigen. Reiche Teile der Bevölkerung verbrennen teure Baumharze, um die Mücken aus
ihren Gemächern zu vertreiben, und werden daher weniger betroffen. Etwa ein Viertel der
ärmeren Bevölkerung wird in den ersten fünf Jahren durch die Krankheit dahingerafft,
erkrankte Reiche Bürger lassen sich von Priestern oder Magiern heilen. In den folgenden
Jahren stirbt die Krankheit nie ganz aus, die Bevölkerung selbst entwickelt jedoch eine
gewisse Resistenz, so dass zumeist nur noch alte und schwache Bürger daran sterben.</p>

<p>Anzeichen für diese Krankheit sind sowohl ein starkes Zittern, Schwindelgefühle,
Orientierungslosigkeit und im späteren Stadium Muskelversagen, schwere, Rauschartige
Halluzinationen und Sinnesverlust, was letztendlich zum Tode führt.</p>

<p>Im Frühjahr des Jahres -1579 findet ein Gynkesischer Einsiedler heraus, dass eine in
den Sümpfen häufig wachsende Pflanze, das Stechkraut, die Krankheit heilt wenn als Tee
getrunken.</p>

<p>Eine Nebenwirkung des Tees ist zudem ein sehr leichter, eher beruhigender und
belebender Rauschzustand, sowie eine Erfrischung der magischen Kraft des Trinkenden.
Sumpfstechtee wird der Haupt-Exportschlager Gynks. Der Tee selbst ist dem Elfenvolk
schon seit Jahrhunderten bekannt, und wird oft vor Meditationen getrunken. Der Handel
mit Gynk erspart den Elfen das Suchen in den wenig angenehmen Sümpfen. In den Schänken
Salkamars und Albars wird Stechkraut-Tee das ultimative Modegetränk, auch Tempel
konsumieren Mengen davon. Die Umgebung Gynks ist seither geprägt durch große
Stechkraut-Farmen.</p>

<p>Als weitere Folge dieser Entdeckung, reisen viele Gelehrte und Kräuterkundige nach
Gynk, um in den Sümpfen nach weiteren unbekannten Kräutern zu suchen.</p>

<p>Aus dieser Zeit stammt wohl auch eine Gynkesische Legende von einem
Riesen-Sumpfkrokodil, vermutlich wurde diese in Umlauf gebracht, um ein größeres
Stechkraut-Vorkommen zu schützen, bevor dieses in Feldern so erfolgreich angebaut
wurde.</p>

