<h2>-1439 Die Schlangenanbeter-Sekte</h2>

<p>In den tieferen Sümpfen Gynks werden Ruinen gefunden. Augenscheinliches Kult-Symbol
in diesen sind grobe Statuen von Menschen, welche einen Schlangen-Unterkörper
besitzen.</p>

<p>In den Ruinen findet sich außer alten Tonkrügen nichts von Wert, auch sind keine
Gräber zu finden, da die Toten jener alten Kultur wohl im Sumpf beerdigt wurden.
Gelehrte finden schnell heraus, dass es sich bei den Bildnissen wohl um die Reaktion
einer primitiven Kultur auf die Begegnung mit Echsenmenschen gehandelt hat.
Aufzeichnungen des nächstgelegenen Tanora-Tempels unterstützen diese These. Da die
Ruinen als uninteressant gelten, werden sie nicht weiter freigelegt und dem Sumpf
überlassen.</p>

<p>Jaergen Erainek, ein ehemaliger salkamaerischer Tempelschüler eines Elara-Schreines,
der Jahre zuvor wegen seiner Vorliebe zu übermäßigem Genus von Stechkraut und anderen
Substanzen von seinen Pflichten .entbunden. worden war und sich seither als Gehilfe bei
diversen Expeditionen verdingte, behauptet jedoch, es handele sich bei den primitiven
Statuen um Abbildungen eines vergessenen Gottes. Er wird verlacht.</p>

<p>Drei Jahre später jedoch hat er eine erstaunliche Anzahl von Anhängern um sich
versammelt, welche in ihm den "Propheten der glorreichen Erleuchtung" sehen. Diese
Anhänger haben in den Ruinen Wohnstätten erbaut, religiöse Hauptpraktik ist das
Konsumieren von Halluzinationen erzeugenden Pflanzen sowie das exportieren
derselben.</p>

