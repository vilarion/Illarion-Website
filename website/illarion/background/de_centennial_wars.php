<h2>-1774 Der Schattenwinter</h2>

<p>Aufgrund des Mangels an Nahrungsreserven bricht im Winter des Jahres 1774 eine
Hungersnot in der Stadt Albar aus. Alten Aufzeichnungen ist zu entnehmen, dass der
Albarische König Theoderich II. angesichts seiner leeren Kornspeicher und Schatzkammern
(Da nahezu alle Reserven in die Versorgung seines Heeres geflossen waren) anordnen ließ,
den Zehnt in den Monaten des Winters fünffach einzutreiben.</p>

<p>Als Folge dieser Handlung verhungerte im kalten Winter beinahe ein Viertel der
gesamten nicht-adeligen Bevölkerung Albars, wiederum ein Viertel der Überlebenden
Leibeigenen . ein Großteil der Bevölkerung . floh im Frühjahr nach Gynk und Salkamar.
Es wird geschätzt, dass etwa ein Drittel der Flüchtlinge entweder an Schwäche starb, in
den warmen Sümpfen Gynks umkam, oder durch Hand Albarischer Gardisten getötet wurde.</p>

<p>Theoderich II. Stirbt im darauffolgenden Sommer an einem vergifteten Pfeil, der ihn
während der Jagd traf.</p>

