<h2>-1777 Plünderung von Firnismühle</h2>

<p>Die orkische Kriegshorde plündert und brandschatzt die Halblingstadt Firnismühle und
umliegende Länder, um Nahrung für den geplanten Kriegszug gegen Albar zu sammeln und um
Sklaven in die Heimatländer zu schicken.</p>

<p>Die Halblinge der Gegend werden unterjocht, organisieren sich jedoch in einem
effektiven Untergrund-Widerstand. Die Zähigkeit und Dickköpfigkeit der Halblinge
bringt ihnen tatsächlich so etwas wie Respekt bei ihren Besatzern ein.</p>

<p>Orkische Sprichwörter aus der Zeit besagen : "Einen toten Halbling ehren Hunderte",
"Der lauteste Tyrann stirbt mit einer Faust im Gemächt", und "Traue keinem Koch, der
dein Sklave ist".</p>

<p>Schon zwei Monate danach trifft ein aus Elfen und Zwergen bestehendes Heer ein,
welche im Namen der Aramon-Allianz die Orkhorde aus Firnismühle vertreiben will, und
unterstützt einen sehr aufgebrachten Mob von Halblingen.</p>

