<h2>-1812 bis -1811 Der Fall der Festung Khalm</h2>

<p>Die legendäre albarische Festung Khalm auf dem Khalmberg (mit der gleichnamigen Stadt
um den Fuß des Berges), angeschlagen durch den langen Krieg mit der salkamaerischen
Grenzstadt Naskur, wird von einer einfallenden Ork-Kriegshorde überfallen, belagert und
innerhalb eines Jahres geschleift. Die albarische Armee befand sich derzeit auf
Vormarsch in Salkamaerisches Gebiet und wurde nicht zur Verteidigung zurückbeordert.</p>

<p>Es heißt, dass die vom Kriegszehnt ausgehungerte und schutzlose Stadt Angesichts der
orkischen Krieger kampflos aufgaben, während nur fünfzig Verteidiger die Festung auf
dem Berg beinahe ein Jahr lang halten konnten. Der damalige Fürst Lhoderik von Khalm bat
beim albarischen König um Verstärkung. Auf die überlieferte Antwort, die er auf sein
Schreiben bekam, soll er tagelang lauthals geflucht haben:</p>

<p style="font-style:italic;">"Wir, Theoderich II., Sohn des glorreichen Wieganth, Erbe
des alten Geschlechts derer von Onatheris, König von Albar und zukünftiger Eroberer von
Salkamar, sind uns der Notwendigkeit nicht gewiss, dass wir unsere glorreiche und baldig
siegreiche Streitmacht von den Grenzen Salkamars abzuziehen gedächten.<br />Wir, Theoderich
II., halten es vielmehr für angemessen, unser vollstes Vertrauen in die Stärke eurer
Verteidigung zu setzen. Wir möchten euch daran erinnern, dass ein gerüsteter albarischer
Krieger, derer ihr über Hunderte verfügen solltet, durchaus in der Lage sei, unter der
richtigen Führung es mit zehnen, wenn nicht gar zwanzig der barbarisch Kreaturen
aufnehmen könnt.<br />
Zudem müssen wir mit Bedauern feststellen, dass ihr wiederholt eurer Pflichten nicht
nachgekommet, den Zehnt zu entrichten. Wir erwarten von euch, Untertan, dieser eurer
Pflicht baldigst Folge zu leisten.. "</p>

<p>In der Legende heißt es, dass der abgetrennte Kopf des Lhoderik von Khalm, der durch
die Axt eines Orkischen Kriegers fiel, aufgesteckt auf einen orkischen Spieß, immer noch
lautstark auf die Dummheit und Arroganz seines Königs fluchte, und den Heerführer der
Orks fluchend um den Gefallen bat, Theoderich ganz kräftig den Allerwertesten zu
versohlen, damit er Ruhe fände.</p>

<p>Da die orkische Horde jedoch geschlagen wurde, bevor sie überhaupt in die Nähe Albars
kam, wurde daraus nichts, und man sagt, dass der Geist Lhoderiks bei Vollmond immer noch
fluchend durch die Ruine ziehe.</p>

