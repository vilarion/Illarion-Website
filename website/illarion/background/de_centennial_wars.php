<h2>-1982 bis -1774 Die Jahrhundertkriege</h2>

<p>Über zwei Jahrhunderte lang bekriegen sich die Städte der Menschen untereinander.
Die anderen Völker halten sich erfolgreich aus diesen Konflikten heraus (Mit Ausnahme
diverser Söldnergruppen, versteht sich). Gegen Ende der Jahrhundertkriege nutzt eine
Orkische Kriegshorde die Gelegenheit und plündert Siedlungen der Menschen und Halblinge.
Als Folge werden die meisten Dörfer und Städte der Menschen zerstört.</p>

<p>Am Ende des Konfliktes gibt es noch drei der vorher fünf großen Menschenstädte: Die
Hauptstädte der alten Reiche Albar und Salkamar und den unabhängigen Freibeuterhafen
Gynk, welcher durch Kriegsflüchtlinge gegründet wurde. Die Kriege werden beendet, weil
Salkamar und Gynk einen Nichtangriffs- und Unterstützungspakt schließen, um die Orkhorde
zu vertreiben. Albar bietet letzten Endes ebenfalls Unterstützung, um sich nicht gegen
die erstarkende Allianz Ehemaliger Kriegsgegner stellen zu müssen.</p>

<p>Da die Orkhorde auf Norodaj-Gebiet flieht und zwischen Nordmänner und Zwerge gerät,
wird sie vollends zersprengt.</p>

