<p><a name="7"></a></p>

<h2>-1702 Aufstände in Albar</h2>

<p>Leibeigenenaufstände in Albar werden durch schwere Reiterei niedergemacht, der
Bauernanführer wird öffentlich zu Tode gepeitscht. In der Legende heißt es, "Rigar, der
Tapfere" habe weder geschrieen noch Anzeichen von Schmerz gezeigt. Alte Dokumente
besagen, dass er sich zuvor in seiner Zelle erhängt hatte. Wie dann jedoch eine
Auspeitschung hätte stattfinden können, wäre fraglich gewesen, also werden diese
Dokumente als fehlerhaft betrachtet. Manche Gelehrte sind der Ansicht, man habe Magie
oder gar Nekromantie verwendet, um den toten Körper zu bewegen, damit die öffentliche
Hinrichtung stattfinden konnte. Die albarische Krone hat auf Gelehrte, welche diese
These verbreiten, jedoch ein Kopfgeld ausgesetzt.</p>

