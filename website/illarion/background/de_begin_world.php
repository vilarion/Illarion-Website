<h2>-5200 Schrift</h2>

<p>Um -900 in Ausgrabungen gefundene schriftliche Elfen-Dokumente werden auf etwa -5200
datiert. Dabei handelt es sich um die ersten "Schriftstücke", die auf ledrigem Pergament
niedergeschrieben wurden, die also Tragbar und flexibel waren. Beim ersten gefundenen
Schriftstück handelt sich anscheinend um ein uraltes Gedicht oder Lied, eine
niedergeschriebene Warnung in Versform:</p>

<p style="font-style:italic;">Manch Wanderers Pfad, ins Dunkel er führt<br />
Durch Schatten von Nabranoo.<br />
Manch steiniger Grat, das Bündel geschnürt<br />
Durch Schatten von Nabranoo.<br />
Verderbnis ist Zeuge von Frevelnder Tat,<br />
Das Dunkel zu meiden sei wertvoller Rat,<br />
Manch Wanderers Herz, im Dunkel verführt<br />
Durch Schatten von Nabranoo.</p>

<p>Es gibt noch keine Geschichtsschreibung. Alle gefundenen Schriftstücke oder Tafeln
vor -5200 (ausschließlich Ton- und Steintafeln, Höhlenmalereien und Fresken - sicher zu
unhandlich für das Schreiben von längeren Texten) dienen ausschließlich
organisatorischen Zwecken. Des Weiteren gibt es auch noch keine echte Jahreszählung,
anscheinend wurde so etwas für unnötig befunden.</p>

<p>Das erste geschichtliche Ereignis, was sich überhaupt an Hand von Aufzeichnungen
genau datieren lässt, sind die Anfänge der Ork-Kriege. Diese Aufzeichnungen stammen
von elfischer Hand und lassen sich durch Abschriften aus den Archiven von Salkamar
belegen.</p>

