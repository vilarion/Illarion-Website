<p>Obwohl die Götter nie wieder die Hand gegeneinander hoben, hatten ihre Kämpfe enormes Misstrauen ausgelöst und immer noch verwüsteten Kriege die Länder. Die jüngeren Götter versuchten die Konflikte durch kleinere, taktische Kriege und endlose Diskussionen zu lösen, doch Argwohn war zwischen den Sterblichen erwachsen. Obgleich Moshran tief im Inneren der Erde gefangen war, hatten seine niederträchtigen Absichten Erfolg auf der Oberfläche. Nach solch bitteren Jahren des Kriegs bevorzugten es die Rassen unter ihresgleichen zu bleiben. Der größte Teil der alten Städte und Orte, die früher von Leuten aller Rassen bewohnt wurden, waren zerstört. Keine von ihnen existierte noch und keiner versuchte sie wieder aufzubauen. Die Saat der Missgunst und des Misstrauens zwischen den Völkern gedieh weiterhin. Einige Rassen bekämpften sich und sogar während Zeiten des Friedens bedrohte die immerwährende Geißel des Krieges die Länder.</p>

<p>Einige Hundert Jahre später entkam der angekettete Moshran aus seiner Zelle. Obwohl klar war, dass ihm jemand geholfen hatte, bestritten seine drei Diener eine Beteiligung daran. Gerüchte verbreiteten sich, dass Nargùn und Cherga seine Ketten gelöst haben, allerdings blieben ihre Gründe unbekannt. Beschützt von seinen treuen Dienern suchte Moshran nach den kostbaren Artefakten, die er als sein Eigen ansah. Noch immer verdorben vom Bösen sehnte er sich nach der Macht des Schwertes und der Rüstung von Madrook Na'han um seine Rache zu bekommen.</p>
 
<p>Als der Gott des Blutes und der Knochen erneut durch die Länder wandelte, kam das Zeitalter der Isolation zum Ende.</p>

