warfen und in großer Angst davon rannten.</p>

<p>Die meisten Echsenmenschen mögen Magie nicht, aber einige von ihnen, besonders diejenigen, die weit entfernt von ihren Städten
leben, haben sich als sehr talentiert erwiesen.</p>

<p>Echsenmenschen essen am liebsten frischen, rohen Fisch, Brot und Früchte. Kuchen und andere süße Speisen scheint ihnen weniger
