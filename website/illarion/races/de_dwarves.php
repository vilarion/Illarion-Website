<p>un begab es sich vor langer Zeit, dass Orkstämme gezwungen waren, neue Jagdgründe zu suchen und dabei in das Territorium
der Zwerge eindrangen. Die Zwerge verteidigten ihre angestammten Gebiete gegen die Orks, doch war ihr Bestreben zum Scheitern
verurteilt allein aufgrund der schieren Masse, die sich ihnen entgegenstellte. Sie zogen sich immer weiter in die tiefen und
unwegsamen Täler der Gebirge zurück und berieten, was zu tun sei. Zum Schutz ihres eigenen Volkes entschieden sie sich, in
die Höhlen und Stollen, welche sie über Jahrzehnte gegraben hatten, zurückzuziehen. Dieser Plan wurde im kommenden Winter
in die Tat umgesetzt und sie verschwanden mit allen ihren Tieren und Habseligkeiten vom Erdboden. Im nächsten Frühjahr
schickten die Orks Kundschafter in alle Täler, doch sie fanden nur verlassene Hütten und Dörfer vor. Diese Nachricht
verbreitete sich wie ein Lauffeuer unter den Orkstämmen. Es kam das Gerücht auf, dass die Zwerge, welche starke und unbeugsame
Gegner waren, von einem mächtigen Fluch heimgesucht wurden und deswegen vernichtet wurden. Selbst die Schamanen der Orks, die
mit Magie den Aufenthaltsort der Zwerge ergründen wollten, konnten sie auf dem Erdboden nicht ausfindig machen. Diese Gerüchte
wurden noch weiter dadurch genährt, dass viele Kundschafter der Orks, die sich auf der Suche nach den Zwergen den Minen
näherten, verschwanden und nie wieder an die Lagerfeuer der Stämme zurückkehrten, so als ob auch sie dem Fluch anheim fielen.</p>

