<p>ie Zwerge sind eine altes Volk. Gemäß den Geschichtsbüchern waren Sie das dritte Volk, welches ein eigenständiges
Schriftbild entwarf. Zu Beginn basierte es noch auf Piktogrammen – eine einfache und leicht zu gravierende Darstellung
der tatsächlichen Bedeutung des Wortes. Es wurde fast ausschließlich dazu genutzt, zum einen die offizielle Geschichte
des Zwergenvolkes niederzuschreiben und zum anderen wurde es zu rein religiösen Zwecken herangezogen. Es hatte somit
eine eher rituelle Bedeutung und entzog sich dem Nutzen des täglichen Lebens. Aber im Laufe der Jahrhunderte entwickelten
sich diese Piktogramme und wurde immer weiter vereinfacht, entsprechend der Notwendigkeit des Volkes zur Nutzung einer
einfachen und allgemeingültigen Schriftsprache. Letzten Endes entstand die noch heute gebrauchte Runenschrift der Zwerge,
deren Wurzeln zwar in den alten Piktogrammen zu finden waren, die aber mittlerweile als eigenständig zu betrachten ist. Nur
noch wenige des Zwergenvolkes sind in der Lage die alten Piktogramme zu lesen oder gar zu schreiben.</p>

<p>Zwerge sind von Natur aus kleiner als Menschen aber in der Regel größer als die Halblinge. Ihre Statur ist kräftig
gebaut mit breiten Schultern und stämmigem Körperbau. Gemäß ihrer Legenden wurden die ersten Zwerge von Irmorom, dem
Gott des Handwerks, aus einem Stück Felsen gehauen, welchen er von Ushara, der Göttin der Erde, erhalten hatte.</p>

