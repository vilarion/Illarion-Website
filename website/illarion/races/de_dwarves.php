<p>ie Katakomben der Zwerge befinden sich ebenfalls unter Tage. Sie werden weit entfernt, häufig tief unter der Stadt der Lebenden
errichtet. Jede Zwergenfamilie hat ein eigenes Familiengrabmal, welches darauf ausgelegt ist, nur das Ehepaar aufzunehmen.
Aber jedes Kind, welches stirbt bevor es geheiratet hat, wird ebenfalls in dieser Gruft zur Ruhe gelegt. Zwerge geben den
Verstorbenen keine goldenen Grabbeigaben mit, da Gold in ihrer Vorstellung weniger ein Element des Todes als das der Lebenden
ist. Die Grabbeigaben der Zwerge sind häufig außergewöhnlich schöne Steine oder Münzen, welche aus Edelsteinen gefertigt wurden.
Diese speziellen Münzen repräsentieren die guten Taten, die ein Zwerg in seinem Leben begangen hat. An einem gesonderten Platz
am Fußende des Grabes wird eine steinerne Stele errichtet, auf dem die Lebensgeschichte des Verstorbenen niedergeschrieben wird.
Darüber hinaus kommt es mitunter bei sehr hochgestellten Persönlichkeiten vor, dass die gesamten Wände der Gruft mit den
traditionellen Piktogrammen graviert werden. Krieger werden häufig auch in Steinrüstungen begraben, ihnen werden Waffen aus
Stein zur Seite gelegt und ihr Gesicht wird von einer aufwendig gestalteten Grabmaske bedeckt. Man sagt sich, dass die Geister
der Verstorbenen den Zwergenklans in Zeiten höchster Not beistehen werden.</p>

