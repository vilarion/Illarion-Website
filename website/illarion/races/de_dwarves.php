<p>werge sind nicht sehr an Magie interessiert. Nur die seltene Runenmagie macht hier eine Ausnahme. Sie bevorzugen
das solide Handwerk, in dem sie wahre Meister werden können. Sie lieben den Glanz des Goldes und den kalten Schimmer
der Edelsteine. Und diese Begierde wird vielleicht nur noch übertroffen vom Anblick eines kühlen und starken
Zwergenbiers, welchem sie wahrlich nur schwer widerstehen können. Ihre Städte und Minen werden erleuchtet durch
verschiedene Lichtquellen: große, dickwandige Glasstrukturen, welche das Licht von den hohen Berggipfeln in die tiefen
Minen umleiten, Fackeln und Laternen, sowie spezielle Edelsteine, welche durch starke Runen zum Glühen angeregt wurden
und zusätzlich auch Wärme abgeben. Bei den Zwergen ist das Gegenstück zu einem Zauberer ein so genannter „Runenmagier“
oder „Runenmeister“ – hierbei wird die Magie mittels geschriebener Runen gewirkt und nicht gesprochen. Diese Art der Magie
wirkt nicht spontan aber ist dafür nicht so unberechenbar und gefährlich, da mögliche Fehlschläge nicht den Wirkenden
schädigen, sondern nur den Gegenstand betreffen, auf dem die Rune gewirkt wurde.</p>

